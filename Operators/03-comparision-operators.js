/* 
Comparision Operators: >, <, >=, <=, ==, !=, ===, !==
*/

let a = 10;
let b = 5;

console.log(a < b); // false

console.log(a > b); // true

console.log(a <= b); // false

console.log(a >= b); // true

console.log(a != b); // true

console.log(a == b); // false

let x = "1";
let y = 1;
console.log(x == y); // true(loose comparision)

console.log(x === y); // false(strict comparision)

console.log(x != y); // false

console.log(x !== y); // true
