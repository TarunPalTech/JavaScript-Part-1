/* 
Arithmetic Operators: +, -, *, /, %, **, ++, --

++ && -- --> Unary Operators
*/

let a = 15;
let b = 5;

console.log(a+b);  // 20

console.log(a-b);  // 10

console.log(a/b);  // 3

console.log(a%b);  // 0

console.log(2**3); // 8

console.log(a++);  // 15 (because it has less priority)

console.log(a);  // 16


console.log(++a); // 17

console.log(b--);  // 5 (less priority and value will be decreased after execution of the function)

console.log(--b);  // 3

