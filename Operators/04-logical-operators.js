/* 
Logical Operators: &&, ||, ! (logical operators are binary operators)
*/

let age = 22;

console.log(age>=20 && age<=30);  // true
// if both of the expressions are true then result will be true otherwise false

console.log(age>=20 || age<=30);  // true
// if any of the expression is true then result will be true

console.log(!(age>20)); // false (It is used to reverse the result)
